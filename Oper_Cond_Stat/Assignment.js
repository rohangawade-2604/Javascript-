let a = 5;
let b = 2;

a += 4; // Equivalent to a = a + 4
console.log("After a += 4, a is", a); // Output: After a += 4, a is 9

a -= 3; // Equivalent to a = a - 3
console.log("After a -= 3, a is", a); // Output: After a -= 3, a is 6

b *= 3; // Equivalent to b = b * 3
console.log("After b *= 3, b is", b); // Output: After b *= 3, b is 6

b /= 2; // Equivalent to b = b / 2
console.log("After b /= 2, b is", b); // Output: After b /= 2, b is 3

a %= 2; // Equivalent to a = a % 2
console.log("After a %= 2, a is", a); // Output: After a %= 2, a is 0

b **= 2; // Equivalent to b = b ** 2
console.log("After b **= 2, b is", b); // Output: After b **= 2, b is 9

