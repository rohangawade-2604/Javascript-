let a = 7
let b = 3

console.log("Addition for a + b is", a + b) // Output: Addition for a + b is 10

console.log("Subtraction for a - b is", a - b) // Output: Subtraction for a - b is 4        

console.log("Multiplication for a * b is", a * b) // Output: Multiplication for a * b is 21

console.log("Division for a / b is", a / b) // Output: Division for a / b is 2.3333333333333335 

console.log("Modulus for a % b is", a % b) // Output: Modulus for a % b is 1 , it returns the remainder of the division

console.log("Exponentiation for a ** b is", a ** b) // Output: Exponentiation for a ** b is 343, it returns a raised to the power of b



// Uniary Operators

// post increment and decrement

console.log(a++) // Output: 7, it returns the value of a and then increments it by 1 , its equivalent to a = a + 1
console.log(a) // Output: 8, it returns the value of a after incrementing it

console.log(a--) // Output: 8, it returns the value of a and then decrements it by 1 , its equivalent to a = a - 1
console.log(a) // Output: 7, it returns the value of a after decrementing it


// pre increment and decrement

console.log(++a) // Output: 8, it increments the value of a by 1 and then returns it, its equivalent to a = a + 1

console.log(++b) // Output: 4, it increments the value of b by 1 and then returns it, its equivalent to b = b + 1